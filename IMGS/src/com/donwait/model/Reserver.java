package com.donwait.model;

import javax.persistence.MappedSuperclass;

@MappedSuperclass
public class Reserver {
	private Integer reserver1;
	private Integer reserver2;
	private String 	reserver3;
	
	public Reserver(){
		reserver1 = 0;
		reserver2 = 0;
		reserver3 = "";
	}
	
	public Integer getReserver1() {
		return reserver1;
	}
	public void setReserver1(Integer reserver1) {
		this.reserver1 = reserver1;
	}
	public Integer getReserver2() {
		return reserver2;
	}
	public void setReserver2(Integer reserver2) {
		this.reserver2 = reserver2;
	}
	public String getReserver3() {
		return reserver3;
	}
	public void setReserver3(String reserver3) {
		this.reserver3 = reserver3;
	}
	
}
